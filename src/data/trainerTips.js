// Motivational Interviewing techniques for trainers
// Adapted from Course Material-6.pdf (Sobell and Sobell, 2008)

export const MI_TECHNIQUES = [
  {
    title: 'Asking Permission',
    rationale: 'Communicates respect for clients. Clients are more likely to discuss changing when asked than when being lectured or told to change.',
    examples: [
      '"Do you mind if we talk about your assessment results?"',
      '"Can we talk a bit about what these scores mean for your daily activities?"',
      '"I noticed some areas we could improve \u2014 do you mind if we discuss them?"',
    ],
  },
  {
    title: 'Eliciting Change Talk',
    rationale: 'Getting clients to voice their own reasons for changing is more effective than lecturing. Change talk from clients is associated with successful outcomes.',
    examples: [
      '"What would you like to see different about your balance/strength?"',
      '"What makes you think it might be time to start exercising more?"',
      '"What would your life look like if you could get up from the floor easily?"',
      '"What would be the good things about improving your balance?"',
    ],
  },
  {
    title: 'Reflective Listening',
    rationale: 'The primary way to build empathy. Listen carefully and paraphrase what the client says to validate their feelings and encourage them to explore their own motivations.',
    examples: [
      '"It sounds like you\'re concerned about falling again..."',
      '"What I hear you saying is that you want to stay independent..."',
      '"So on one hand you want to improve, and on the other you\'re worried about the time commitment..."',
    ],
  },
  {
    title: 'Normalizing',
    rationale: 'Communicates that having difficulties is not uncommon. Helps clients understand they are not alone in their experience.',
    examples: [
      '"A lot of people your age notice their balance isn\'t what it used to be."',
      '"Many of our clients felt the same way when they first saw their scores."',
      '"It\'s very common to score below norms at first \u2014 that\'s exactly why this program exists."',
    ],
  },
  {
    title: 'Decisional Balancing',
    rationale: 'Help clients evaluate their current situation by looking at both the good and less-good aspects. This helps them understand their ambivalence about changing.',
    examples: [
      '"What are some benefits of your current activity level? And what are some downsides?"',
      '"On the flip side, what would be the less-good things about starting a training program?"',
    ],
  },
  {
    title: 'Importance & Confidence',
    rationale: 'Rating scales help you understand how the client views change and can be used to get them to voice what they need to do.',
    examples: [
      '"On a scale of 1-10, how important is it to you to improve your balance?"',
      '"Why did you pick a 6 instead of a 3?"',
      '"What would need to happen for that number to move up to an 8?"',
    ],
  },
  {
    title: 'Affirmations',
    rationale: 'Recognize client strengths, successes, and efforts. Builds confidence in their ability to change. Must sound genuine, not overly enthusiastic.',
    examples: [
      '"Your commitment to coming in for this assessment shows real dedication."',
      '"The fact that you completed every test today shows a lot of determination."',
      '"You showed real strength on that sit-to-stand test."',
    ],
  },
  {
    title: 'Presenting Results',
    rationale: 'Present feedback in a neutral, non-judgmental way that empowers clients to make informed decisions. Compare their results to norms so they can see where they stand.',
    examples: [
      '"Here\'s how your scores compare to others in your age group \u2014 what do you notice?"',
      '"Your TUG time puts you in a category where research shows increased fall risk. What are your thoughts on that?"',
      '"Your grip strength is actually above the norm for your age \u2014 that\'s a real strength to build on."',
    ],
  },
]
